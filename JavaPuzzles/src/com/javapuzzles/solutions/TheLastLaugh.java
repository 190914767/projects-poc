package com.javapuzzles.solutions;

public class TheLastLaugh {

	public static void main(String[] args) {

		System.out.println("H" + "A"); // HA
		System.out.println('H' + ""+ 'A');// HA
		System.out.println('H' + 'A'); // 137

		System.out.println("2 + 2 " +2+2); // 2 + 2 22 
		System.out.printf("%c%c", 'H','A'); // HA
	}
}
